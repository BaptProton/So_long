#include "../so_long.h"

int first_sort(char **map, int x, int y)
{
    if (map[y - 1][x] == '0')
        map[y - 1][x] == 'N';
    if (map[y + 1][x] == '0')
        map[y + 1][x] == 'S';
    if (map[y][x + 1] == '0')
        map[y][x + 1] == 'A';
    if (map[y][x - 1] == '0')
        map[y][x - 1] == 'W';
    if (map[y - 1][x] == 'N')
        return (map, x, y - 1);
    
    
    
        
}